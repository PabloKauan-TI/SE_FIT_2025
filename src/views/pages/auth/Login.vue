<script setup>
import FloatingConfigurator from '@/components/FloatingConfigurator.vue';
import userService from '@/service/UserService';
import { ref } from 'vue';
import { useRouter } from 'vue-router';

const email = ref('');
const password = ref('');
const router = useRouter();

const Login = async () => {
    try {
        const response = await userService.login({
            email: email.value,
            password: password.value
        });

        // ðŸ’¡ Ajuste para verificar o token na resposta do Axios
        if (response.data && response.data.token) {
            localStorage.setItem('token', response.data.token);
            localStorage.setItem('type', response.data.user['type']);
            router.push('/');
        } else {
            // Este bloco Ã© para logins que retornam 200, mas sem o token
            alert('Falha no login: Resposta inesperada.');
        }
    } catch (error) {
        console.error('Erro ao fazer login:', error);

        // ðŸ’¡ CORREÃ‡ÃƒO DE ERRO NO CATCH: Acessa a mensagem do erro HTTP (se existir) de forma segura
        const apiMessage = error.response?.data?.message;

        if (apiMessage) {
            alert(apiMessage);
        } else {
            // Para erros de rede, timeout, ou quando error.response Ã© indefinido
            alert('Falha na comunicaÃ§Ã£o com o servidor. Tente novamente.');
        }
    }
};
</script>

